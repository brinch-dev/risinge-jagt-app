-- v1.8.1: Add UPDATE policies for CRUD on hunt_areas, hunt_events, towers

-- Allow admin to update hunt_areas
CREATE POLICY "Admin can update hunt_areas"
  ON hunt_areas FOR UPDATE
  TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM profiles
      WHERE profiles.id = auth.uid()
      AND profiles.role = 'admin'
    )
  )
  WITH CHECK (
    EXISTS (
      SELECT 1 FROM profiles
      WHERE profiles.id = auth.uid()
      AND profiles.role = 'admin'
    )
  );

